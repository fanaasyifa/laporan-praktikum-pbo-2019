    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Peminjaman pinjem = new Peminjaman();
        pinjem.setIdPeminjaman(Integer.parseInt(txtIdPeminjam.getText()));
//        pinjem.setAnggota((Anggota)txtIdAnggota.getText());
//        pinjem.setBuku(txtIdBuku.getText());
        pinjem.setTanggalPinjam(txtTanggalPinjam.getText());
        pinjem.setTanggalKembali(txtTanggalKembali.getText());
        pinjem.save();
        
        txtIdPeminjam.setText(Integer.toString(pinjem.getIdPeminjaman()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPeminjam.getModel();
        int row = tblPeminjam.getSelectedRow();
        
        Peminjaman peminjaman = new Peminjaman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        peminjaman.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

