    private void tblPeminjamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPeminjam.getModel();
        int row = tblPeminjam.getSelectedRow();
        Peminjaman peminjaman = new Peminjaman();
        
        peminjaman = peminjaman.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdPeminjam.setText(String.valueOf(peminjaman.getIdPeminjaman()));
        txtIdAnggota.setText(String.valueOf(peminjaman.getAnggota().getIdanggota()));
        txtIdBuku.setText(String.valueOf(peminjaman.getBuku().getIdbuku()));
        txtTanggalPinjam.setText(peminjaman.getTanggalPinjam());
        txtTanggalKembali.setText(peminjaman.getTanggalKembali());
        
    }//GEN-LAST:event_tblPeminjamMouseClicked

