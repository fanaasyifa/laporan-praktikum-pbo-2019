    public void tampilkanData(){
        String[] kolom = {"ID", "ID Anggota", "ID Buku", "[TGL] Pinjam", "[TGL] Kembali"};
        ArrayList<Peminjaman> list = new Peminjaman().getAll();
        Object rowData[] = new Object[5];
        
        tblPeminjam.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for(int i = 0; i < list.size(); i++){
            rowData[0] = list.get(i).getIdPeminjaman();
            rowData[2] = list.get(i).getBuku().getIdbuku();
            rowData[1] = list.get(i).getAnggota().getIdanggota();
            rowData[3] = list.get(i).getTanggalPinjam();
            rowData[4] = list.get(i).getTanggalKembali();
            
            ((DefaultTableModel)tblPeminjam.getModel()).addRow(rowData);
        }
    }
    
    public void cariAnggota(String keyword){
        ArrayList<Peminjaman> list = new Peminjaman().searchIdAnggota(keyword);
//        Object rowData[] = new Object[1];

        for(Peminjaman pinjem : list){
            lblAnggota.setText(pinjem.getAnggota().getNama());
        }
    }
    
    public void cariBuku(String keyword){
        ArrayList<Peminjaman> list = new Peminjaman().searchIdBuku(keyword);
        for(Peminjaman pinjem : list){
            lblBuku.setText(pinjem.getAnggota().getNama());
        }
    }
    
